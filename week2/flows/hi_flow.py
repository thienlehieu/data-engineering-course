def write_local(df: pd.DataFrame, color: str, dataset_file: str) -> Path:
    """Write DataFrame out locally as parquet file"""
    path = Path(f"week2/data/{color}/{dataset_file}.parquet")
    df.to_parquet(path, compression="gzip")
    return path

